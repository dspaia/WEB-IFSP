//Exemplo 3 - Estações

let mes = 'Janeiro';

switch (mes) {
    case 'Dezembro':
    case 'Janeiro':
    case 'Fevereiro':
        console.log("Verão!"); break

    case 'Março':
    case 'Abril':
    case 'Maio':
        console.log("Outono!"); break

    case 'Junho':
    case 'Julho':
    case 'Agosto':
        console.log("Inverno!"); break

    case 'Setembro':
    case 'Outubro':
    case 'Novembro':
        console.log("Primavera!"); break

    default: break
}